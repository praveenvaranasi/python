# The following Program performs all the available list Operations
sample_list = [1, 2, 3, 4, 5]
sample_list.append(6)   # append Operation
print(sample_list)
sample_list.insert(2, 7)    #insert operation
print(sample_list)
sample_list.append(1)
print(sample_list)
sample_list.remove(1)   #remove operation
sample_list.pop()      #pop Operation
print(sample_list)
sample_list.pop()      #pop Operation
print(sample_list)
sample_list.pop(3)
